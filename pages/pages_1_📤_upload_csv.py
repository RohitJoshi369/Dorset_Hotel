import streamlit as st
import pandas as pd

st.title("üì§ Upload CSV File")

uploaded_file = st.file_uploader("Upload CSV File (.csv)", type=["csv"])

if uploaded_file is not None:
    try:
        df = pd.read_csv(uploaded_file)

        required_columns = [
            "House Name",
            "Room Number",
            "Bhagat Name 1",
            "Bhagat Name 2"
        ]

        if not all(col in df.columns for col in required_columns):
            st.error("CSV file does not contain required columns.")
        else:
            st.session_state["data"] = df
            st.success("CSV uploaded successfully ‚úÖ")
            st.write("‚û°Ô∏è Go to **Search Bhagat** page")

    except Exception as e:
        st.error(f"Error reading file: {e}")